package com.bayviewglen.daythree;

public class ExampleOne {

	public static void main(String[] args) {
		
		System.out.println("There is \"an\" issue!");
		// the \ is meant to escape special characters such as " "
		// \" is an example of an escape sequence
		
		System.out.println("This is a \\ did you see it?");
		
		System.out.println("This is a line break \nI used \"\\n\" to do this.");
		System.out.println("This is a tab. \t I used \"\\t\" to do it.");
		
	}

}
